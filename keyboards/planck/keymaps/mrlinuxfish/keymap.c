// Led Layer code
void keyboard_pre_init_user(void) {
    setPinOutput(D3);  // initialize Layer 0 led
    setPinOutput(D2);  // initialize Layer 1 led
    setPinOutput(D1);  // initialize Layer 2 led
    setPinOutput(B3);  // initialize Layer 3 led
}

layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case _RAISE:  //Layer 0
            writePinHigh(D3);
            writePinLow(D2);
            writePinLow(D1);
            writePinLow(B3);
            break;
        case _LOWER:  //Layer 1
            writePinHigh(D2);
            writePinLow(D3);
            writePinLow(D1);
            writePinLow(B3);
            break;
        case _COLEMAK:  //Layer 2
            writePinHigh(D1);
            writePinLow(D3);
            writePinLow(D2);
            writePinLow(B3);
        case _PLOVER:  //Layer 3
            writePinHigh(B3);
            writePinLow(D3);
            writePinLow(D2);
            writePinLow(D1);
        default:
            writePinLow(D3);
            writePinLow(D2);
            writePinLow(D1);
            writePinLow(B3);
            break;
    }
    return update_tri_layer_state(state, _LOWER, _RAISE, _ADJUST);
}
